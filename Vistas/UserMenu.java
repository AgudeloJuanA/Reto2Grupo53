    private void listarEmpleados(){
        String nombre = txtBuscarEmp.getText();
        if (nombre.isEmpty()){
            String queryConsulta= "SELECT * FROM empleados";
            try{
                connection = conexion.getConnection();
                st = connection.createStatement();
                rs = st.executeQuery(queryConsulta);
                Object[] empleados = new Object[6];
                contenidoTablaEmpleados = (DefaultTableModel)tblEmpleados.getModel();
                while(rs.next()){
                    empleados[0] = rs.getInt("idEmp");
                    empleados[1] = rs.getString("nombreEmp");
                    empleados[2] = rs.getString("apellidos");
                    empleados[3] = rs.getString("tipoDocumento");
                    empleados[4] = rs.getString("documento");
                    empleados[5] = rs.getString("correo");
                    contenidoTablaEmpleados.addRow(empleados);
                    System.out.println(rs.getInt("idEmp")+ " " 
                            + rs.getString("nombreEmp")+ " " + rs.getString("apellidos"));
                    
                }
                tblEmpleados.setModel(contenidoTablaEmpleados); 
            }catch (SQLException e){
                System.out.println("Error");
        
            }
                
        }else{
        String queryConsulta="SELECT * FROM empleados WHERE nombreEmp LIKE '%"+nombre+"%' OR apellidos LIKE '%"+nombre+"%' ";
            try{
                connection = conexion.getConnection();
                st = connection.createStatement();
                rs = st.executeQuery(queryConsulta);
                Object[] empleados = new Object[6];
                contenidoTablaEmpleados = (DefaultTableModel)tblEmpleados.getModel();
                while(rs.next()){
                    empleados[0] = rs.getInt("idEmp");
                    empleados[1] = rs.getString("nombreEmp");
                    empleados[2] = rs.getString("apellidos");
                    empleados[3] = rs.getString("tipoDocumento");
                    empleados[4] = rs.getString("documento");
                    empleados[5] = rs.getString("correo");
                    contenidoTablaEmpleados.addRow(empleados);
                    System.out.println(rs.getInt("idEmpleados")+ " " 
                            + rs.getString("nombreEmp")+ " " + rs.getString("apellidos")+rs.getString("tipoDocumento")+rs.getString("documento")
                    +rs.getString("correo"));
                    
                }        
                tblEmpleados.setModel(contenidoTablaEmpleados);    
                
            }catch (SQLException e){
                System.out.println("Error");
        } 
       }
    }
    
    private void borrarDatosTabla(){
        for(int i = 0;i < tblEmpleados.getRowCount();i++){
        contenidoTablaEmpleados.removeRow(i);
        i-=1;
        }
    }
    
    

